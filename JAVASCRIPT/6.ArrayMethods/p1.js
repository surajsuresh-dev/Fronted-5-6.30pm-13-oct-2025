
//push() and pop() -LILO

var users = ['tom', 'Sam'];
console.log(`users =`, users);

//push(val) -> store
users.push('Harry');
console.log(`users =`, users);

users.push('John');
console.log(`users =`, users);

console.log(`\n`);

//pop() -> remove
users.pop();
console.log(`users =`, users);

users.pop();
console.log(`users =`, users);

users.pop();
console.log(`users =`, users);